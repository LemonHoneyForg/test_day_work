
public class AverageScore
{
	public static void main(String[] args)
	{
		int score1 = 80;
		int score2 = 97;
		int score3 = 85;
		double total = score1 + score2 + score3;
		double average = total / 3;
		System.out.println("Total: " + total);
		System.out.println("Average " + average);
	}// end of main
}// end of class
